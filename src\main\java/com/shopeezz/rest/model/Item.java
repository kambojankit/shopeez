package com.shopeezz.rest.model;

public class Item {
	String itemName;
	String itemId;
	String itemQuantity;
	String itemPrice;
	String itemQuantityType;
	
	public String getItemName() {
		return itemName;
	}
	public void setItemName(String itemName) {
		this.itemName = itemName;
	}
	public String getItemId() {
		return itemId;
	}
	public void setItemId(String itemId) {
		this.itemId = itemId;
	}
	public String getItemQuantity() {
		return itemQuantity;
	}
	public void setItemQuantity(String itemQuantity) {
		this.itemQuantity = itemQuantity;
	}
	public String getItemPrice() {
		return itemPrice;
	}
	public void setItemPrice(String itemPrice) {
		this.itemPrice = itemPrice;
	}
	public String getItemQuantityType() {
		return itemQuantityType;
	}
	public void setItemQuantityType(String itemQuantityType) {
		this.itemQuantityType = itemQuantityType;
	}
	
	
}
